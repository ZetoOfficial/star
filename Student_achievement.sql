create table speciality
(
    id_speciality SERIAL PRIMARY KEY,
    speciality_name VARCHAR(70)
);

create table study_group
(
	id_group SERIAL PRIMARY KEY,
	group_name VARCHAR(50),
	fk_id_speciality INT,
    foreign key(fk_id_speciality) references speciality (id_speciality)
);

create table student
(
	id_student SERIAL PRIMARY KEY,
	student_fullname VARCHAR(70),
	student_birth DATE,
	fk_id_group INT,
    foreign key(fk_id_group) references study_group (id_group)	
);

create table teacher
(
	id_teacher SERIAL PRIMARY KEY,
	teacher_fullname VARCHAR(70),
	teacher_department VARCHAR(50),
	teacher_position VARCHAR(50)
);

create table discipline
(
	id_discipline SERIAL PRIMARY KEY,
	discipline_name VARCHAR(50)
);

create table mark
(
	id_mark SERIAL PRIMARY KEY,
	mark_control_form VARCHAR(50),
	mark_name REAL,
	mark_delivery_date DATE,
	fk_id_discipline INT,
    foreign key(fk_id_discipline) references discipline (id_discipline),
    fk_id_teacher INT,
    foreign key(fk_id_teacher) references teacher (id_teacher),
    fk_id_student INT,
    foreign key(fk_id_student) references student (id_student)
);

